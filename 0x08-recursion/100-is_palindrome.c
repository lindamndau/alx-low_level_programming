/**
 * Main - This function returns 1 if a string is a palindrome and 0 if not.
 * An empty string is a palindrome
 * Return: Char *s
 */
int is_palindrome(char *s)
{
	int length = 0;
	if(length == 0 || length == 1)
	{
		return (1);
	}
	else
	{
		return (0);
	}
	return (*s);
}
